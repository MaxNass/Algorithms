package new_In_Java_8;

public class SimpleOffer {

    private int id;
    private String site;

    public SimpleOffer(int id, String site) {
        this.id = id;
        this.site = site;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSite() {
        return site;
    }

    public void setSite(String site) {
        this.site = site;
    }
}
